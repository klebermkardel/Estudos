// Calculadora básica em JavaScript

// Declara dois números fixos
const number1 = 10;
const number2 = 5;

// Operações matemáticas
const soma = number1 + number2;
const subtracao = number1 - number2;
const multiplicacao = number1 * number2;
const divisao = number1 / number2;

// Exibe os resultados no console
console.log(`Número 1: ${number1}`);
console.log(`Número 2: ${number2}`);
console.log(`Soma: ${soma}`);
console.log(`Subtração: ${subtracao}`);
console.log(`Multiplicação: ${multiplicacao}`);
console.log(`Divisão: ${divisao}`);