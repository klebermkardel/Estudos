// Operadores Aritméticos: +, - , * , / , ** 

let salario = 100

console.log(salario + salario) // exibe 200
console.log(salario - salario) // exibe 0
console.log(salario * salario) // exibe 10000
console.log(salario / salario) // exibe 1
console.log(5 ** 5) // exibe 3125

// Operadores Incremento/Decremento
// ++, -- 

// Exemplos:

// Incremento

let idade = 18
console.log(idade++) // exibe 18
console.log(idade) // exibe 19

console.log(++idade) // exibe 20 diretamente

// Decremento

console.log(idade--) // exibe 20
console.log(idade) // exibe 19

console.log(--idade) // exibe 18 diretamente