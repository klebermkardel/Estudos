// Math-floor -> retorna o número inteiro arrendodado para baixo

// Exemplos: 

// Retorno esperado: 5
console.log(Math.floor(5.95));

// Retorno esperado: 5
console.log(Math.floor(5.05));

// Retorno esperado: 5
console.log(Math.floor(5));

// Retorno esperado: -6
console.log(Math.floor(-5.05));




