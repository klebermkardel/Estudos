// Declarando variáveis
let A;

// Calculando o valor absoluto de -40
A = Math.abs(-40);
console.log("Valor Absoluto de -40:", A);

// Calculando 2^8
A = Math.pow(2, 8);
console.log("2^8:", A);

// Calculando a raiz quadrada de 36
A = Math.sqrt(36);
console.log("Raiz Quadrada de 36:", A);

// Obtendo o valor de Pi
A = Math.PI;
console.log("Pi:", A);

// Calculando o seno de 0.523
A = Math.sin(0.523);
console.log("Seno de 0.523:", A);

// Calculando o cosseno de 0.523
A = Math.cos(0.523);
console.log("Cosseno de 0.523:", A);

// Calculando a tangente de 0.523
A = Math.tan(0.523);
console.log("Tangente de 0.523:", A);

// Convertendo 30 graus para radianos
A = (30 * Math.PI) / 180;
console.log("Grau para Rad de 30:", A);
