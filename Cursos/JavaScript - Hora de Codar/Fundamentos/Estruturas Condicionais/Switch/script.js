// Estruturas Condicionais (Switch)

// Switch

const fruta = "Maçã";

switch (fruta) {
    case "Banana":
        console.log("Banana é a fruta");
        break;
    case "Maçã":
        console.log("Maçã é a fruta");
        break;
    default: // Executa script caso nenhum dos cases sejam atendidos
        console.log("Fruta não encontrada");
}