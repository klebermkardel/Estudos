// Fundamentos - Estruturas de Repetição

// Loop For
console.log("Loop For")

// Estrutura Loop For: contador, condição de limite, incremento
for(let i = 0; i <= 5; i++) {
    console.log(i);
}

console.log("---------------------");



// Loop While
console.log("Loop While");

let i = 0;

while(i <= 5) {
    console.log(i);
    i++;
}

console.log("---------------------");


// Loop Do While
console.log("Loop Do While")

let j = 0;

do {
    console.log(j);
    j++;
} while (j <= 5)
