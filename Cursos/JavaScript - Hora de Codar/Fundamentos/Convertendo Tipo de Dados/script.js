// Fundamentos - Convertendo Tipos de Dados

// Conversão de Tipos
const meuNumero = "123";
const meuNumeroConvertido = Number(meuNumero); // converte string para número
console.log(meuNumero); // retorna "123"
console.log(typeof meuNumero); // retorna string
console.log(meuNumeroConvertido); // retorna 123
console.log(typeof meuNumeroConvertido); // retorna number

