/* Exercício 2: Conversão de Tipos

Dado o valor de uma string "1234", converta-o em número e exiba o tipo da variável no console

*/

const valorString = "1234";
const valorNumber = Number(valorString);

console.log(`${valorString}: ${typeof(valorString)}`)
console.log(`${valorNumber}: ${typeof valorNumber}`);