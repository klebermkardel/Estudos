// Leia três números e exiba a média aritmética deles.

const n1 = parseFloat(prompt("Digite o 1º número:"))
const n2 = parseFloat(prompt("Digite o 2º número:"))
const n3 = parseFloat(prompt("Digite o 3º número:"))

const media = (n1+n2+n3) / 3

alert(`A média entre os três números informados é ${media}`)