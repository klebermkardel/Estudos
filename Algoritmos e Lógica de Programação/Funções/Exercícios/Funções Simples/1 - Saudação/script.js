// Saudação: Crie uma função chamada exibirSaudacao que, ao ser chamada, imprima a mensagem "Olá! Bem-vindo ao meu programa!" no console. Chame essa função para ver a mensagem.

// 1. DECLARAÇÃO DA ARROW FUNCTION
// Criamos uma constante com o nome da função.
// A sintaxe () => {} representa a função.
// () -> lista de parâmetros (vazia neste caso)
// => -> a "flecha" que caracteriza a arrow function
// {} -> o corpo da função
const exibirSaudacaoArrow = () => {
    console.log("Olá! Bem-vindo ao meu programa!");
};

// 2. CHAMADA DA ARROW FUNCTION
// A chamada é feita da mesma forma.
exibirSaudacaoArrow();