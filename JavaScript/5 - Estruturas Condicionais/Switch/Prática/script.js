/*

2. Exemplo Prático: Organizando sua Semana

Vamos usar os dias da semana para decidir qual será o foco do seu estudo:

*/

const diaDaSemana = "segunda"

switch(diaDaSemana) {
    case "segunda":
        console.log("Foco em Lógica de Programação.")
        break
    case "terça":
        console.log("Dia de praticar Operadores.")
        break
    case "quarta":
        console.log("Revisão de Condicionais")
        break
    default:
        console.log("Dia de estudo livre ou descanso.")
}